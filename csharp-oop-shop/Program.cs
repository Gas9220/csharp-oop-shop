// Product class definition
public class Product
{
    private int code;
    private string name;
    private string description;
    private float price;
    private int vat;

    public Product(int code, string name, string description, float price, int vat) {
        this.code = code;
        this.name = name;
        this.description = description;
        this.price = price;
        this.vat = vat; 
    }
}
